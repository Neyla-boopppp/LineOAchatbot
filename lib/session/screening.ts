// เทียบชื่อแบรนด์แบบยืดหยุ่น — ผู้ใช้พิมพ์ได้หลายแบบ (Khao So-i / khaosoi / ข้าวซอย)
export function isForeignEligibleBrand(brand: string | null | undefined): boolean {
  if (!brand) return false
  const n = brand.toLowerCase().replace(/[\s\-_.]/g, '')
  return n.includes('khaosoi') || n.includes('ข้าวซอย')
}

